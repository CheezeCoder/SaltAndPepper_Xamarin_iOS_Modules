using System;
using UIKit;

namespace TextFieldTokenRecognitionAndHandling 
{
	///<summary>
	///
	///</summary>
	public class CustomTextField : UITextField
	{
		//========================================================================================================================================
		//  PRIVATE CLASS PROPERTIES
		//========================================================================================================================================
		//========================================================================================================================================
		//  PUBLIC CLASS PROPERTIES
		//========================================================================================================================================
		public event EventHandler didDelete;
		//========================================================================================================================================
		//  Constructor
		//========================================================================================================================================
		/// <summary>
		/// Initializes a new instance of the <see cref="TextFieldTokenRecognitionAndHandling.CustomTextField"/> class.
		/// </summary>
		public CustomTextField (CoreGraphics.CGRect frame) : base(frame)

		{
			
		}
		//========================================================================================================================================
		//  PUBLIC OVERRIDES
		//========================================================================================================================================
		public override void DeleteBackward ()
		{
			didDelete (this, EventArgs.Empty);
			base.DeleteBackward ();
		}
		//
		//========================================================================================================================================
		//  PUBLIC METHODS
		//========================================================================================================================================
		//========================================================================================================================================
		//  PRIVATE METHODS
		//========================================================================================================================================
	}
}
